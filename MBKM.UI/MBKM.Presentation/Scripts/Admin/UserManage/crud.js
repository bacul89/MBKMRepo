
function AddUserTemplate() {
    if ($('#created-user-template').length) {
        $('#TambahUser').modal('show');
    } else {
        $.ajax({
            url: '/Admin/UserManage/ModaladdUser',
            type: 'get',
            datatype: 'html',
            success: function (e) {
                console.log(e);
                if ($('.data-content-modal').length) {
                    $('.data-content-modal').remove();
                }
                $('#modal-inner').append(e);
                $('.modal').modal('show');
            }
        })
    }
}

